package fr.sweetiez.api.sweet;

public class Sweet {
    private final String id;
    private final String name;
    private final String category;
    private final Double price;

    public Sweet(String id, String name, String category, Double price) {
        this.id = id;
        this.name = name;
        this.category = category;
        this.price = price;
    }

    @Override
    public String toString() {
        return "Sweet{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", category='" + category + '\'' +
                ", price=" + price +
                '}';
    }
}
